<h1>Introducción</h1>

<p>
Bienvenido al centro de documentación del proyecto babel. Entre estas
paginas encontrará la mayor parte de las respuestas respecto al uso,
instalación, administración y desarrollo de este sistema.
</p>
<p>
Si aún asi no encontró lo que buscaba entre estas paginas, puede escribir un 
correo electronico a 
<a href="mailto: babel@scesi.memi.umss.edu.bo">babel@scesi.memi.umss.edu.bo</a>
