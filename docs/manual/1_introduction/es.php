de estudiantes de sistemas e informatica</a> (SCESI). Su apoyo en la busqueda de
errores, correcciones, ambigüedades, o mejoras será apreciada.
</p>

<h2>¿Qué es Babel?</h2>
<p>
Basicamente el sitio es un repositorio de documentos pdf, con facilidades para
la busqueda, clasificación, y ordenamiento de grandes cantidades. Aún asi puede
referirse al <a href="http://wiki.scesi.memi.umss.edu.bo/index.php/Babel">wiki
de la Sociedad Cientifica</a> para ver las anecdotas del surgimiento, avances, y
discusiones del proyecto.
</p>
