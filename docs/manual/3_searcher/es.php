<h1>Funciones de busqueda</h1>

<p>
Como se vio anteriormente, con el uso del motor de busqueda Lucene, se ha dado
a la función de busqueda en Babel un amplio conjunto de posibilidades. A
continuación se describen los campos existentes en el motor de busqueda.
</p>

<h2>Terminos disponibles</h2>
<p>
Los terminos disponibles en el buscador de babel son: "title", "author", 
"publisher", "language", "year", y "filename", pudiendo tambien utilizar sus casi
equivalentes en español "titulo", "autor", "editorial", "idioma", "año", y
"archivo".
</p>

<h2>Campos por omisión</h2>
<p>
No se definio un campo por omisión, de esta forma cualquier busqueda que no
utilice notación de campos, será buscada en cualquiera de los disponibles.
</p>
