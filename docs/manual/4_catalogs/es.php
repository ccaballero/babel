</div>

<h2>
<img title="Folcsonomía" alt="Folcsonomía" src="/media/img/icons/arrow_divide.png">
&nbsp;Catalogos folcsonomicos</h2>
<p>
Se dice que un catalogo es
<a href="http://es.wikipedia.org/wiki/Folcsonom%C3%ADa">folcsonomico</a> cuando
un libro que pertenezca a ese catalogo, puede estar en una o varias de sus ramas.
</p>
<p>
Como ejemplo de catalogo folcsonomico, podemos citar el pensum de una carrera,
donde un libro podría facilmente servir a los propositos de mas de una materia.
</p>
