<h1>Manejo de catalogos</h1>

<p>
Babel ademas provee la funcionalidad de manejar catalogos de libros.Los
catalogos, pueden ser de dos tipos: taxonomicos o folcsonomicos.
</p>
<p>
Estos manejan el patrón de etiquetado colaborativo, donde cada usuario puede
crear un sistema diferente de interpretación de la realidad, y puede hacer de
este un catalogo cerrado (<img title="Catálogo cerrado" alt="Catálogo cerrado" src="/media/img/icons/lock.png">
) o abierto (<img title="Catálogo abierto" alt="Catálogo abierto" src="/media/img/icons/key.png">) a la colaboración de otros usuarios.
</p>

<h2>
<img title="Taxonomía" alt="Taxonomía" src="/media/img/icons/arrow_join.png">
&nbsp;Catalogos taxonomicos</h2>
<p>
Se dice que un catalogo es
<a href="http://es.wikipedia.org/wiki/Taxonom%C3%ADa">taxonomico</a> cuando un
libro que pertenezca a ese catalogo, unicamente pueda estar en una de sus ramas.
</p>
<p>
Como ejemplo de catalogo taxonomico, podemos citar la
<a href="http://es.wikipedia.org/wiki/Sistema_Dewey_de_clasificaci%C3%B3n">
Clasificación decimal de Dewey</a>, usada frecuentemente en Bibliotecas, como
un metodo para ordenar sus libros.
</p>
