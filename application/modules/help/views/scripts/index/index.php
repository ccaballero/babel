<h1>Ayuda</h1>

<p>
En la Torre de Babel<br />
vivían cincuenta cigarros<br />
vivían amontonados<br />
hechos todos de papel<br />
</p>
<p>
Uno a uno alineados<br />
todos muy bien formados<br />
El más pequeño era aquel<br />
y se llamaba Gabriel<br />
</p>
<p>
Tabaco fino, algodones<br />
iban a ver el río<br />
Iban cantando canciones,<br />
muertos de frío<br />
La pasión de Gabriel<br />
era nadar en el río<br />
Le contó de esto a su tío<br />
que era un cigarro de miel<br />
</p>
<p>
Su tío que era muy viejo<br />
no le puso reparos<br />
y cuendo llegó el habano<br />
forrado entero de café<br />
"Dónde se metió Gabriel"<br />
preguntó inquieto el habano<br />
"Se fue con otros cigarros<br />
a ver el río correr"<br />
"No lo puedo creer.<br />
Eres muy mal ciudadano,<br />
pues tu sobrino y hermano<br />
tal vez nunca pueda volver"<br />
</p>
<p>
Cuando llegó el habano<br />
a la orilla del río<br />
vió un pequeño cigarro<br />
muerto, abierto & tendido.<br />
Estaba todo mojado<br />
sin señales de vida.<br />
El tabaco se desprendía,<br />
quedaba desparramado<br />
</p>
<p>
Tristeza, llantos, lamentos<br />
reinarán desde hoy en Babel.<br />
Se nos ha muerto Gabriel,<br />
todo ha quedado en silencio.<br />
</p>
<p>
Solamente un buen fuego<br />
puede dar muerte a un cigarro<br />
pero si muere mojado es igual<br />
que si a un hombre<br />
lo mataran colgado<br />
colgado <br />
</p>
